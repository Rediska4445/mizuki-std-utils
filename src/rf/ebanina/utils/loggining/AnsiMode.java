package rf.ebanina.utils.loggining;

/**
 * Перечисление AnsiMode представляет режимы ANSI-форматирования текста.
 * <p>
 * Содержит набор стандартных текстовых эффектов ANSI, таких как жирный,
 * курсив, подчеркивание, мигание и прочие декоративные модификаторы.
 * Каждый эффект связан с соответствующим ANSI-escape кодом.
 * </p>
 * <p>
 * Предоставляет методы для получения управляющей ANSI-последовательности
 * для включения эффекта и статический метод сброса всех эффектов.
 * </p>
 * <p>
 * Основные возможности:
 * <ul>
 *   <li>Генерация ANSI escape последовательностей для форматирования текста.</li>
 *   <li>Простой и удобный API для включения и сброса текстовых эффектов.</li>
 *   <li>Демонстрация использования форматов в методе main с примерами логирования.</li>
 * </ul>
 * </p>
 *
 * <h3>Пример использования</h3>
 * <pre>{@code
 * // Включение жирного текста:
 * System.out.println(AnsiMode.BOLD.sequence(false) + "Жирный текст" + AnsiMode.reset());
 *
 * // Сброс эффектов:
 * System.out.println(AnsiMode.reset() + "Нормальный текст");
 * }</pre>
 *
 * @author Ebanina Std
 * @version 1.4.4
 * @since 0.0.9
 * @see Log
 */
public enum AnsiMode {
    /** Сброс всех ANSI режимов и эффектов. */
    RESET("0"),
    /** Жирный текст. */
    BOLD("1"),
    /** Тусклый (dim) текст, затемнённый. */
    DIM("2"),
    /** Курсивный текст. */
    ITALIC("3"),
    /** Подчёркнутый текст. */
    UNDERLINE("4"),
    /** Мигание текста. */
    BLINK("5"),
    /** Инверсия цвета (фон и текст меняются местами). */
    INVERSE("7"),
    /** Скрытый текст. */
    HIDDEN("8"),
    /** Зачёркнутый текст. */
    STRIKETHROUGH("9");
    /** ANSI код эффекта */
    private final String code;
    /**
     * Конструктор, связывающий режим с ANSI кодом.
     *
     * @param code код ANSI как строка.
     */
    AnsiMode(String code) {
        this.code = code;
    }
    /**
     * Возвращает ANSI управляющую последовательность для включения данного режима.
     * <p>
     * Параметр {@code isBackground} не используется, так как данное перечисление
     * представляет режимы форматирования, а не цвета.
     * </p>
     *
     * @param isBackground не используется.
     * @return ANSI escape последовательность для включения режима (например, "\u001B[1m").
     */
    public String sequence(boolean isBackground) {
        return "\u001B[" + code + "m";
    }
    /**
     * Сброс всех атрибутов ANSI.
     * @return строка сброса "\u001B[0m"
     */
    public static String reset() {
        return "\u001B[0m";
    }
}